maze = [
    [9, 9, 9, 9, 9, 9, 9, 9, 9],
    [9, 0, 0, 0, 9, 9, 0, 9, 9],
    [9, 0, 9, 9, 0, 0, 0, 0, 9],
    [9, 0, 0, 0, 0, 9, 9, 9, 9],
    [9, 9, 0, 9, 0, 9, 0, 1, 9],
    [9, 0, 0, 9, 0, 0, 0, 9, 9],
    [9, 0, 9, 0, 0, 9, 0, 0, 9],
    [9, 9, 9, 9, 9, 9, 9, 9, 9]
]

d = [[0, -1], [-1, 0], [0, 1], [1, 0]]

x, y = 1, 1
count = 0
dir = 0

while maze[x][y] != 1:
    move = d[(dir + 1) % 4]

    if maze[x + move[0]][y + move[1]] != 9:
        x = x + move[0]
        y = y + move[1]

        dir = (dir + 1) % 4

        count = count + 1
        print(f'[{x}, {y}], count={count}')
    else:
        dir = (dir + 3) % 4
